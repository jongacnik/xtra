<div class="subs-row times">
	<h2 class="century">Reviews</h2>
	<p>Reviews are generally 500–4000 words. X-TRA is committed to publishing reviews that critically and thoroughly engage the work of art as it is experienced in the public sphere, believing that the encounter between the work of art and an informed, impartial viewer is the core experience that manifests our collective cultural investment in and hope for visual arts today. Reviews should promote critical dialogue, rather than report on or evaluate works of art. They may cover individual or group exhibitions, installations, screenings and performances; or they may examine one or more publications. We prefer that references be placed within the text, but endnotes are acceptable.</p>
</div>
<div class="subs-row times">
	<h2 class="century">Features</h2>
	<p>Ranging from 2,000–10,000 words, feature articles may be scholarly research; they may be biographies of or interviews with artists or critics; they may use an event, exhibition, book, video, etc. as a jumping-off point for a discussion of larger economic, political and cultural issues. Use of endnotes is expected but not required. </p>
</div>
<div class="subs-row times">
	<h2 class="century">Columns</h2>
	<p>Columns (generally from 500–3000 words) may be written in a more “subjective” voice and may be on or about virtually any subject in the domain of visual art and cultural criticism. Endnotes are permitted. </p>
</div>
<div class="subs-row times">
	<h2 class="century">Artist's Projects</h2>
	<p>Artists are invited to submit original ideas for direct-to-print artworks for publication. Proposals that address the content, context and format of the magazine are favored. As of Volume 15, our pages are 6.5 inches wide x 9.75 inches tall. Selected projects will be allocated up to four pages. Proposals that incorporate a web-based element for publication at www.x-traonline.org are encouraged. Submissions must include a complete description of the proposed project, an artist's statement, representations of the project or previous work, and the contact information.</p>
</div>
<div class="subs-row times">
	<h2 class="century">Honoraria</h2>
