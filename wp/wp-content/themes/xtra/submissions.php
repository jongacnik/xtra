	<h2 class="century">Submission Guidelines</h2>
	<p>X-TRA is a journal of record. Writers should imagine their articles in the long term and consider aspects of artistic production they feel are significant, eschewing newsy reports or judgments for more extended and reasoned inquiry. X-TRA’s editorial board reserves the right to refuse articles more appropriate for other settings.

